<?php 

// Untuk mendeklarasikan sebuah variable gunakan tanda dollar lalu definisikan nama variable tersebut.
$name = "John Doe";

// Menampilkan variable name menggunakan echo
echo $name;

// Menggabungkan variable dengan string menggunakan notasi dot (.)
echo 'Hello my name is' . $name;

// atau menggunakan double string tanpa harus menggunakan notasi dot (.)
echo "Hello my name is {$name}";